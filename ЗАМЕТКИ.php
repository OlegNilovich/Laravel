Что такое фасады?
- Это удобный интерфейс взаимодействия с экземпляром класса

Как они работают?
1 Мы обращаемся к методу фасада Post::findOrFail($id)
2 В специальном контейнере создается экземпляр определенного класса
3 Если таковой экземпляр класса уже есть, то возвращается этот экземпляр
4 После чего вызывается метод данного экземпляра класса и туда передаются аргументы
___________________________________________________________________________________
Что такое сервис-провайдеры?
- Это своего рода доступ к экземпляру класса в контейнере в момент обращения
Доступ осуществляется через Функции-хелперы и Фасады

Для чего они нужны?
- Для удобства: чтобы экземпляр класса создавался в момент обращения к его методу
- Для оптимизации: не нужно держать в памяти все возможные экземплры классов
