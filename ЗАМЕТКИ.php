Что такое SPA?
Существует три основных подхода к разработке веб-приложений:
- одностраничные (SPA) делаются на VUE.js и React
- многостраничные (MPA) Multi Page Application
- прогрессивные (PWA)
Single Page Applicatoin - это приложение, которое работает в рамках одной HTML 
страницы и обеспечивает динамическое обновление контента без перезагрузки страницы

SSR (Server side rendering) - Nuxt.js ДЛЯ SPA

